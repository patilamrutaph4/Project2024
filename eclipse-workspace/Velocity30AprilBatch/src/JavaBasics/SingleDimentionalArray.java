package JavaBasics;

public class SingleDimentionalArray {
	public static void main(String[] args) {
		//Declaration
				int a[]=new int[3];
		
		//Adding value in array
				a[0]=30;
				a[1]=61;
				a[2]=57;
				
		//Finding size of the array
				System.out.println("Array is of the size:-" + a.length);
	
				
		//Reading array
				for(int i=0; i<a.length; i++) 
				
				{
					System.out.print(a[i]+ " ");
					
				}
				}

}
