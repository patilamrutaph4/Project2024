package JavaBasics;

public class StaticNonstaticMethod {

	//static method
	public static void t1() {
		System.out.println("Static method t1");
	}
	public static void t2() {
		System.out.println("Static method t2");
	}
	
		
	//Non static method
	
	public void t3() {
		System.out.println("Non static method t3");
	}
	
	public void t4() {
		System.out.println("Non static method t4");
	}
}
